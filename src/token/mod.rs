#[derive(Debug, PartialEq, Clone, Eq)]
pub enum Token {
    Illigal,
    Eof,
    Ident(String),
    Int(i64),
    Assign,
    Plus,
    Minus,
    Bang,
    Asterisk,
    Slash,
    Lt,
    Gt,
    Equal,
    Comma,
    Semicolon,
    LParen,
    RParen,
    LBrace,
    RBrace,
    Function,
    Let,
    If,
    Else,
    Boolean(bool),
    Return,
    NotEqual,
    String(String),
    LBacket,
    RBacket,
    Colon,
    Macro,
}

pub fn token_from_literal(lieral: String) -> Token {
    match lieral.as_ref() {
        "let" => Token::Let,
        "fn" => Token::Function,
        "true" => Token::Boolean(true),
        "false" => Token::Boolean(false),
        "return" => Token::Return,
        "if" => Token::If,
        "else" => Token::Else,
        "=" => Token::Assign,
        "+" => Token::Plus,
        "-" => Token::Minus,
        "!" => Token::Bang,
        "*" => Token::Asterisk,
        "/" => Token::Slash,
        "<" => Token::LT,
        ">" => Token::GT,
        "," => Token::Comma,
        ";" => Token::Semicolon,
        "(" => Token::LParen,
        ")" => Token::RParen,
        "{" => Token::LBrace,
        "}" => Token::RBrace,
        "==" => Token::Equal,
        "!=" => Token::NotEqual,
        "[" => Token::RBracket,
        "]" => Token::LBracket,
        ":" => Token::Colon,
        "macro" => Token::Macro,
        _ => Token::Ident(lieral),
    }
}